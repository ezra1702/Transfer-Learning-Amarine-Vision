"""
    Operator keanggotaan
        => Digunakan untuk memeriksa apakah suatu nilai atau variabel terdapat dalam sebuah urutan (seperti string, list, tuple, set, atau dictionary).
        1. in        : Memeriksa apakah suatu nilai ada dalam urutan.
        2. not in    : Memeriksa apakah suatu nilai tidak ada dalam urutan.
    
"""

anggota = ['sharleen', 'aleta', 'manajemen', 22]

print(f" 'manajemen' in anggota : {'manajemen' in anggota} ")
print(f" 'teknik' not in anggota : {'teknik' not in anggota} ")
print(f" 22 in anggota : {22 in anggota} ")
print(f" 'aleta' not in anggota : {'aleta' not in anggota} ")