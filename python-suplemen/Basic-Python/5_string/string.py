name = "Sharleen Aletta"
print(name.upper())        # SHARLEEN ALETTA
print(name.lower())        # sharleen aletta
print(name.title())        # Sharleen Aletta
print(name.capitalize())   # Sharleen aletta
print(name.replace("Aletta", "Smith"))  # Sharleen Smith
print(name.split())       # ['Sharleen', 'Aletta']
print("-".join(name.split()))  # Sharleen-Aletta
print(name.find("Sharleen"))  # 9
print(name.count("e"))    # 3
print(name.startswith("Shar"))  # True
print(name.endswith("tta"))    # True
print(len(name))          # 15
print(name.endswith("sinta"))    # True
print(name[0:5])
print(name[:-1])

